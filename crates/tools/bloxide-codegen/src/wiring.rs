//! Generate a binary `main.rs` from a `WiringConfig`.

use crate::schema::{WiringActorConfig, WiringConfig, WiringConnectionConfig};
use quote::{format_ident, quote};
use std::collections::{BTreeMap, BTreeSet, HashSet};

const HEADER: &str = "// Copyright 2025 Bloxide, all rights reserved\n// Auto-generated by bloxide-codegen. Do not edit manually.\n";

fn crate_name(s: &str) -> String {
    s.replace("-", "_")
}

fn blox_to_actor_name(blox: &str) -> String {
    let mut chars = blox.chars();
    match chars.next() {
        None => String::new(),
        Some(first) => first.to_uppercase().collect::<String>() + &chars.as_str().to_lowercase(),
    }
}

/// Determine the incoming message type and channel capacity for each actor from
/// the connection graph. Each actor gets one channel per unique incoming
/// message type; for this codegen we use the first declared connection.
pub fn actor_messages(
    actors: &[WiringActorConfig],
    connections: &[WiringConnectionConfig],
) -> BTreeMap<String, (String, usize)> {
    let mut map: BTreeMap<String, (String, usize)> = BTreeMap::new();
    for conn in connections {
        map.entry(conn.to.clone())
            .or_insert((conn.message.clone(), conn.channel_capacity.unwrap_or(16)));
    }
    // Ensure every declared actor has an entry, even if it has no incoming
    // connections, so downstream codegen can always reference an id.
    for actor in actors {
        map.entry(actor.name.clone()).or_insert((String::new(), 16));
    }
    map
}

pub fn generate(config: &WiringConfig, _crate_name: &str) -> anyhow::Result<String> {
    validate(config)?;

    let is_tokio = config.runtime == "tokio";
    let is_embassy = config.runtime == "embassy";
    if !is_tokio && !is_embassy {
        anyhow::bail!(
            "unsupported wiring runtime '{}', expected 'tokio' or 'embassy'",
            config.runtime
        );
    }

    let runtime_crate_ident = format_ident!(
        "{}",
        if is_tokio {
            "bloxide_tokio"
        } else {
            "bloxide_embassy"
        }
    );
    let runtime_ident = format_ident!(
        "{}",
        if is_tokio {
            "TokioRuntime"
        } else {
            "EmbassyRuntime"
        }
    );

    let actor_msg_map = actor_messages(&config.actors, &config.connections);
    let actors_by_name: BTreeMap<String, &WiringActorConfig> =
        config.actors.iter().map(|a| (a.name.clone(), a)).collect();

    // ── Imports ─────────────────────────────────────────────────────────────
    let mut use_stmts = Vec::new();
    use_stmts.push(quote! {
        use ::#runtime_crate_ident::prelude::*;
    });
    use_stmts.push(quote! {
        use ::bloxide_core::lifecycle::LifecycleCommand;
    });

    for actor in &config.actors {
        let blox_crate_ident = format_ident!("{}", crate_name(&format!("{}-blox", actor.blox)));
        use_stmts.push(quote! {
            use ::#blox_crate_ident::prelude::*;
        });
    }

    let mut actions_traits: BTreeMap<String, BTreeSet<String>> = BTreeMap::new();
    for actor in &config.actors {
        if !actor.behavior_traits.is_empty() && actor.behavior.is_some() {
            let actions_crate = crate_name(&format!("{}_actions", actor.blox));
            actions_traits
                .entry(actions_crate)
                .or_default()
                .extend(actor.behavior_traits.iter().cloned());
        }
    }
    for (actions_crate, traits) in actions_traits {
        let actions_crate_ident = format_ident!("{}", actions_crate);
        let trait_idents: Vec<_> = traits.into_iter().map(|t| format_ident!("{}", t)).collect();
        use_stmts.push(quote! {
            use ::#actions_crate_ident::{#(#trait_idents),*};
        });
    }

    // ── Channel creation and actor id extraction ──────────────────────────────
    let mut channel_stmts = Vec::new();
    for actor in &config.actors {
        let ref_ident = format_ident!("{}_ref", actor.name);
        let mbox_ident = format_ident!("{}_mbox", actor.name);
        let id_ident = format_ident!("{}_id", actor.name);

        if let Some((message, capacity)) = actor_msg_map.get(&actor.name) {
            if !message.is_empty() {
                let message_ident = format_ident!("{}", message);
                let capacity_lit = proc_macro2::Literal::usize_unsuffixed(*capacity);
                channel_stmts.push(quote! {
                    let ((#ref_ident,), #mbox_ident) = ::#runtime_crate_ident::channels! {
                        #message_ident(#capacity_lit),
                    };
                    let #id_ident = #ref_ident.id();
                });
                continue;
            }
        }
        // Actor with no incoming connections: create a dummy id so downstream
        // generated code can still reference it. Real wiring always has at
        // least one connection per actor.
        channel_stmts.push(quote! {
            let #id_ident = ::bloxide_core::messaging::ActorId::default();
        });
    }

    // ── Actor task declarations ───────────────────────────────────────────────
    let mut task_decls = Vec::new();
    for actor in &config.actors {
        let actor_name = blox_to_actor_name(&actor.blox);
        let spec_ident = format_ident!("{}Spec", actor_name);
        let task_ident = format_ident!("{}_task", actor.name);
        let spec_ty = if let Some(behavior) = &actor.behavior {
            let behavior_ident = format_ident!("{}", behavior);
            quote! { #spec_ident<#runtime_ident, #behavior_ident> }
        } else {
            quote! { #spec_ident<#runtime_ident> }
        };
        task_decls.push(quote! {
            ::#runtime_crate_ident::actor_task_supervised!(#task_ident, #spec_ty);
        });
    }

    // ── Context construction ──────────────────────────────────────────────────
    let mut ctx_stmts = Vec::new();
    for actor in &config.actors {
        let actor_name = blox_to_actor_name(&actor.blox);
        let ctx_ident = format_ident!("{}Ctx", actor_name);
        let ctx_var_ident = format_ident!("{}_ctx", actor.name);
        let id_ident = format_ident!("{}_id", actor.name);

        let mut ctor_args = vec![quote! { #id_ident }];
        for (_field_name, source_actor) in &actor.context_fields {
            let source_ident = format_ident!("{}_ref", source_actor);
            ctor_args.push(quote! { #source_ident.clone() });
        }
        if let Some(behavior) = &actor.behavior {
            let behavior_ident = format_ident!("{}", behavior);
            ctor_args.push(quote! { #behavior_ident::default() });
        }

        ctx_stmts.push(quote! {
            let #ctx_var_ident = #ctx_ident::new(#(#ctor_args),*);
        });
    }

    // ── Machine construction ──────────────────────────────────────────────────
    let mut machine_stmts = Vec::new();
    for actor in &config.actors {
        let machine_ident = format_ident!("{}_machine", actor.name);
        let ctx_var_ident = format_ident!("{}_ctx", actor.name);
        machine_stmts.push(quote! {
            let #machine_ident = ::bloxide_core::StateMachine::new(#ctx_var_ident);
        });
    }

    // ── Supervisor wiring ─────────────────────────────────────────────────────
    let mut supervisor_stmts = Vec::new();
    let has_supervisors = !config.supervisors.is_empty();
    if has_supervisors {
        supervisor_stmts.push(quote! {
            let mut group = ::bloxide_supervisor::registry::ChildGroupBuilder::new(
                ::bloxide_supervisor::registry::GroupShutdown::WhenAnyDone,
            );
        });

        for sup in &config.supervisors {
            for child in &sup.children {
                let child_actor = actors_by_name.get(&child.actor).ok_or_else(|| {
                    anyhow::anyhow!(
                        "supervisor '{}' child '{}' not declared in [[wiring.actors]]",
                        sup.name,
                        child.actor
                    )
                })?;
                let mbox_ident = format_ident!("{}_mbox", child_actor.name);
                let id_ident = format_ident!("{}_id", child_actor.name);
                let machine_ident = format_ident!("{}_machine", child_actor.name);
                let task_ident = format_ident!("{}_task", child_actor.name);
                let policy = if let Some(max) = child.restart_max {
                    quote! { ::bloxide_supervisor::registry::ChildPolicy::Restart { max: #max } }
                } else {
                    quote! { ::bloxide_supervisor::registry::ChildPolicy::Stop }
                };

                if is_tokio {
                    supervisor_stmts.push(quote! {
                        ::#runtime_crate_ident::spawn_child!(
                            group,
                            #task_ident(#machine_ident, #mbox_ident, #id_ident),
                            #policy
                        );
                    });
                } else {
                    supervisor_stmts.push(quote! {
                        ::#runtime_crate_ident::spawn_child!(
                            spawner,
                            group,
                            #task_ident(#machine_ident, #mbox_ident, #id_ident),
                            #policy
                        );
                    });
                }
            }
        }

        supervisor_stmts.push(quote! {
            let _sup_control_ref = group.control_ref();
            let _sup_notify = group.notify_sender();
            let sup_id = ::#runtime_crate_ident::next_actor_id!();
            let (children, sup_notify_rx, sup_control_rx) = group.finish();
        });

        let supervisor_spec_path: syn::Path =
            syn::parse_str("::bloxide_supervisor::supervisor::SupervisorSpec")
                .expect("valid supervisor spec path");
        let supervisor_ctx_path: syn::Path =
            syn::parse_str("::bloxide_supervisor::supervisor::SupervisorCtx")
                .expect("valid supervisor ctx path");
        let supervisor_event_path: syn::Path =
            syn::parse_str("::bloxide_supervisor::event::SupervisorEvent")
                .expect("valid supervisor event path");

        supervisor_stmts.push(quote! {
            let sup_ctx = #supervisor_ctx_path::new(sup_id, children);
            let mut sup_machine = ::bloxide_core::StateMachine::<#supervisor_spec_path<#runtime_ident>>::new(sup_ctx);
            sup_machine.dispatch(#supervisor_event_path::<#runtime_ident>::Lifecycle(LifecycleCommand::Start));
        });
    }

    // Spawn/run supervisor task — must be inside the main/setup function body,
    // not at file level (it's a statement, not an item).
    let run_supervisor = if has_supervisors {
        if is_tokio {
            quote! {
                supervisor_task(sup_machine, (sup_notify_rx, sup_control_rx)).await;
            }
        } else {
            quote! {
                spawner.must_spawn(supervisor_task(sup_machine, (sup_notify_rx, sup_control_rx)));
            }
        }
    } else {
        quote! {}
    };

    // ── Main function ─────────────────────────────────────────────────────────
    let main_fn = if is_tokio {
        quote! {
            async fn main() {
                #(#channel_stmts)*
                #(#ctx_stmts)*
                #(#machine_stmts)*
                #(#supervisor_stmts)*
                #run_supervisor
            }
        }
    } else {
        quote! {
            fn main() {
                static EXECUTOR: ::static_cell::StaticCell<::embassy_executor::Executor> =
                    ::static_cell::StaticCell::new();
                let executor = EXECUTOR.init(::embassy_executor::Executor::new());
                executor.run(setup);
            }

            fn setup(spawner: ::embassy_executor::Spawner) {
                #(#channel_stmts)*
                #(#ctx_stmts)*
                #(#machine_stmts)*
                #(#supervisor_stmts)*
                #run_supervisor
            }
        }
    };

    // Root task is only needed when there is a supervisor tree.
    let root_task_decl = if has_supervisors {
        let supervisor_spec_path: syn::Path =
            syn::parse_str("::bloxide_supervisor::supervisor::SupervisorSpec")
                .expect("valid supervisor spec path");
        if is_tokio {
            quote! {
                ::#runtime_crate_ident::root_task!(supervisor_task, #supervisor_spec_path<#runtime_ident>);
            }
        } else {
            quote! {
                ::#runtime_crate_ident::root_task!(
                    supervisor_task,
                    #supervisor_spec_path<#runtime_ident>,
                    ::core::process::exit(0)
                );
            }
        }
    } else {
        quote! {}
    };

    let tokens = quote! {
        #(#use_stmts)*
        #(#task_decls)*
        #root_task_decl
        #main_fn
    };

    let file = syn::parse2::<syn::File>(tokens)
        .map_err(|e| anyhow::anyhow!("syn parsing failed: {}", e))?;
    let mut formatted = prettyplease::unparse(&file);

    // prettyplease cannot parse the `#[tokio::main]` attribute when it is
    // generated through quote, so we prepend it to the formatted main function.
    if is_tokio {
        formatted = formatted.replace("async fn main()", "#[tokio::main]\nasync fn main()");
    }

    Ok(format!("{}{}", HEADER, formatted))
}

fn validate(config: &WiringConfig) -> anyhow::Result<()> {
    let actor_names: HashSet<String> = config.actors.iter().map(|a| a.name.clone()).collect();

    for actor in &config.actors {
        if actor.blox.trim().is_empty() {
            anyhow::bail!("wiring actor '{}' has empty blox name", actor.name);
        }
    }

    for conn in &config.connections {
        if !actor_names.contains(&conn.from) {
            anyhow::bail!(
                "wiring connection from '{}' to '{}' references unknown actor '{}'",
                conn.from,
                conn.to,
                conn.from
            );
        }
        if !actor_names.contains(&conn.to) {
            anyhow::bail!(
                "wiring connection from '{}' to '{}' references unknown actor '{}'",
                conn.from,
                conn.to,
                conn.to
            );
        }
    }

    for actor in &config.actors {
        for (field_name, source_actor) in &actor.context_fields {
            if !actor_names.contains(source_actor) {
                anyhow::bail!(
                    "wiring actor '{}' context_field '{}' references unknown actor '{}'",
                    actor.name,
                    field_name,
                    source_actor
                );
            }
        }
    }

    for sup in &config.supervisors {
        for child in &sup.children {
            if !actor_names.contains(&child.actor) {
                anyhow::bail!(
                    "supervisor '{}' child '{}' not declared in [[wiring.actors]]",
                    sup.name,
                    child.actor
                );
            }
        }
    }

    Ok(())
}
