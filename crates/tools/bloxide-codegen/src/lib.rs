use schema::{BloxConfig, SystemConfig};
use std::collections::BTreeMap;
use std::path::Path;

/// Generate all artifacts from a parsed `BloxConfig`.
///
/// Returns a vector of `(filename, content)` pairs.
pub fn generate_all(
    config: &BloxConfig,
    crate_name: &str,
) -> anyhow::Result<Vec<(String, String)>> {
    let mut files = Vec::new();

    if let Some(messages) = &config.messages {
        for msg in messages {
            let code = messages::generate(msg)?;
            files.push((format!("messages_{}.rs", msg.name.to_lowercase()), code));
        }
    }

    if let Some(event) = &config.event {
        let code = events::generate(event)?;
        files.push(("events.rs".to_string(), code));
    }

    if let Some(topology) = &config.topology {
        let actor_name = config.actor.as_ref().map(|a| a.name.as_str());
        let code = topology::generate(topology, actor_name, crate_name)?;
        files.push(("topology.rs".to_string(), code));
    }

    if let Some(context) = &config.context {
        let code = ctx::generate(context, crate_name)?;
        files.push(("ctx.rs".to_string(), code));
    }

    if let (Some(actor), Some(topology), Some(event), Some(context)) = (
        &config.actor,
        &config.topology,
        &config.event,
        &config.context,
    ) {
        let code = spec_skeleton::generate(actor, topology, context, event, crate_name)?;
        files.push(("spec_skeleton.rs".to_string(), code));
    }

    if let Some(wiring) = &config.wiring {
        if !wiring.actors.is_empty() {
            let code = wiring::generate(wiring, crate_name)?;
            files.push(("wiring_main.rs".to_string(), code));
        }
    }

    if let Some(mailboxes) = &config.mailboxes {
        let code = mailboxes::generate(mailboxes)?;
        files.push(("mailboxes_impls.rs".to_string(), code));
    }

    // Generate mod.rs that re-exports all generated submodules
    if !files.is_empty() {
        let mod_rs = generate_mod_rs(&files);
        files.push(("mod.rs".to_string(), mod_rs));
    }

    Ok(files)
}

fn generate_mod_rs(files: &[(String, String)]) -> String {
    let mut lines = vec![
        "// Copyright 2025 Bloxide, all rights reserved".to_string(),
        "// Auto-generated module.".to_string(),
        "// Files in this directory are generated by bloxide-codegen.".to_string(),
    ];

    for (filename, _) in files {
        if filename == "mod.rs" {
            continue;
        }
        let stem = filename.strip_suffix(".rs").unwrap_or(filename);
        lines.push(format!("pub mod {stem};"));
        lines.push(format!("#[allow(unused_imports)] pub use {stem}::*;"));
    }

    lines.join("\n") + "\n"
}

/// Parse a `blox.toml` file and generate all artifacts.
///
/// The crate name is inferred from the parent directory name.
pub fn generate_from_toml(input_path: &Path) -> anyhow::Result<Vec<(String, String)>> {
    let content = std::fs::read_to_string(input_path)?;
    let config: BloxConfig = toml::from_str(&content)?;

    let crate_name = input_path
        .parent()
        .and_then(|p| p.file_name())
        .and_then(|n| n.to_str())
        .unwrap_or("unknown")
        .to_string();

    generate_all(&config, &crate_name)
}

/// Generate artifacts and write them to a directory.
///
/// Files are written only if their content has changed, preserving mtime for
/// caching.
pub fn generate_to_dir(
    config: &BloxConfig,
    crate_name: &str,
    output_dir: &Path,
) -> anyhow::Result<Vec<String>> {
    std::fs::create_dir_all(output_dir)?;

    let files = generate_all(config, crate_name)?;
    let mut written = Vec::new();

    for (filename, content) in files {
        let path = output_dir.join(&filename);
        let needs_write = if path.exists() {
            std::fs::read_to_string(&path)? != content
        } else {
            true
        };

        if needs_write {
            std::fs::write(&path, &content)?;
            written.push(filename);
        }
    }

    Ok(written)
}

/// Generate a system wiring binary (main.rs) from a system.toml manifest.
///
/// Reads the system.toml at `system_path`, discovers all blox.toml files in the
/// workspace, parses them into BloxConfig entries, and calls
/// `system_wiring::generate` to produce the main.rs content.
pub fn generate_system_wiring_from_toml(
    system_path: &Path,
    workspace_root: &Path,
) -> anyhow::Result<String> {
    let content = std::fs::read_to_string(system_path)?;
    let config: SystemConfig = toml::from_str(&content)?;

    // Discover all blox.toml files in the workspace.
    let mut blox_configs = BTreeMap::new();
    for entry in walkdir::WalkDir::new(workspace_root)
        .into_iter()
        .filter_map(|e| e.ok())
    {
        if entry.file_name() == "blox.toml" {
            let blox_content = std::fs::read_to_string(entry.path())?;
            let blox_config: BloxConfig = toml::from_str(&blox_content)?;
            // Use the parent directory name as the key (e.g. "ping" for crates/bloxes/ping/blox.toml)
            // But the system.toml references blox crates by their crate name (e.g. "ping-blox")
            // so we need to derive the crate name from the Cargo.toml in the same directory.
            let dir = entry.path().parent().unwrap();
            let cargo_toml_path = dir.join("Cargo.toml");
            let key = if cargo_toml_path.exists() {
                let cargo_content = std::fs::read_to_string(&cargo_toml_path)?;
                // Parse [package] name = "..." from Cargo.toml
                cargo_content
                    .lines()
                    .find_map(|line| {
                        let trimmed = line.trim();
                        if trimmed.starts_with("name = ") {
                            let name = trimmed
                                .strip_prefix("name = ")
                                .unwrap()
                                .trim()
                                .trim_matches('"');
                            Some(name.to_string())
                        } else {
                            None
                        }
                    })
                    .unwrap_or_else(|| dir.file_name().unwrap().to_string_lossy().to_string())
            } else {
                dir.file_name().unwrap().to_string_lossy().to_string()
            };
            blox_configs.insert(key, blox_config);
        }
    }

    let generated = system_wiring::generate(&config, &blox_configs)?;
    Ok(generated)
}
