use bloxide_codegen::schema::BloxConfig;
use bloxide_codegen::{generate_all, generate_from_toml};

#[test]
fn test_parse_counter_messages_toml() {
    let toml = r#"
[[messages]]
name = "CounterMsg"
visibility = "pub"

[[messages.variants]]
name = "Tick"
"#;

    let config: BloxConfig = toml::from_str(toml).expect("parse failed");
    let messages = config.messages.expect("messages section missing");
    assert_eq!(messages.len(), 1);
    assert_eq!(messages[0].name, "CounterMsg");
    assert_eq!(messages[0].variants.len(), 1);
    assert_eq!(messages[0].variants[0].name, "Tick");
    assert!(messages[0].variants[0].fields.is_empty());
}

#[test]
fn test_generate_counter_messages() {
    let toml = r#"
[[messages]]
name = "CounterMsg"
visibility = "pub"

[[messages.variants]]
name = "Tick"
"#;

    let config: BloxConfig = toml::from_str(toml).expect("parse failed");
    let files = generate_all(&config, "counter-messages").expect("generate failed");

    let msg_file = files.iter().find(|(n, _)| n == "messages_countermsg.rs").expect("messages file missing");
    let (_name, content) = msg_file;

    // Header
    assert!(content.contains("// Auto-generated by bloxide-codegen. Do not edit manually."));

    // Unit struct for empty fields
    assert!(content.contains("pub struct Tick;"));

    // Enum with wrapper variant
    assert!(content.contains("pub enum CounterMsg {"));
    assert!(content.contains("Tick(Tick),"));

    // Derives
    assert!(content.contains("#[derive(Debug, Clone, Copy)]"));

    // message_name method
    assert!(content.contains("pub fn message_name(&self) -> &'static str {"));
    assert!(content.contains("CounterMsg::Tick(..) => \"Tick\""));
}

